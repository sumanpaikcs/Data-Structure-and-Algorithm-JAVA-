package WHILEloop;
public class whileloop {
    public static void main(String[] args){
        // While loop in java


        int i = 0;
        while(i < 5){
            System.out.println("this is while loop");
            i++;
        }

        // do while loops

        int d = 0;
        do{
            System.out.println("this is Do while loop");
            d++;
        }
        while(d < 5);
    }
}

